package Contador01;

public class Cambalhota {

    public static void main (String[] args) {
        int cc = 1;

        while (cc <= 5) {
            System.out.println("Cambalhota " + cc);
            //para incrementar o indice podemos utilizar:
            // variavel++ ou variavel = variavel + 1
            cc++;

        }
    }
}
